package com.gelora.absensi.model;

public class ListChatMate {

    private String avatar;
    private String nik;
    private String nama;
    private String message;
    private String time;
    private String belum_dibaca;
    private String last_sender;
    private String read_status;

    public String getAvatar() {
        return avatar;
    }

    public void setAvatar(String avatar) {
        this.avatar = avatar;
    }

    public String getNik() {
        return nik;
    }

    public void setNik(String nik) {
        this.nik = nik;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getBelum_dibaca() {
        return belum_dibaca;
    }

    public void setBelum_dibaca(String belum_dibaca) {
        this.belum_dibaca = belum_dibaca;
    }

    public String getLast_sender() {
        return last_sender;
    }

    public void setLast_sender(String last_sender) {
        this.last_sender = last_sender;
    }

    public String getRead_status() {
        return read_status;
    }

    public void setRead_status(String read_status) {
        this.read_status = read_status;
    }
}
