package com.gelora.absensi.model;


public class PelangganList {

    private String idPelanggan;
    private String kategoriPelanggan;
    private String tipePelanggan;
    private String namaPelanggan;
    private String alamatPelanggan;
    private String picPelanggan;
    private String teleponPelanggan;
    private String keteranganKunjunganPelanggan;

    public String getIdPelanggan() {
        return idPelanggan;
    }

    public void setIdPelanggan(String idPelanggan) {
        this.idPelanggan = idPelanggan;
    }

    public String getKategoriPelanggan() {
        return kategoriPelanggan;
    }

    public void setKategoriPelanggan(String kategoriPelanggan) {
        this.kategoriPelanggan = kategoriPelanggan;
    }

    public String getNamaPelanggan() {
        return namaPelanggan;
    }

    public void setNamaPelanggan(String namaPelanggan) {
        this.namaPelanggan = namaPelanggan;
    }

    public String getAlamatPelanggan() {
        return alamatPelanggan;
    }

    public void setAlamatPelanggan(String alamatPelanggan) {
        this.alamatPelanggan = alamatPelanggan;
    }

    public String getPicPelanggan() {
        return picPelanggan;
    }

    public void setPicPelanggan(String picPelanggan) {
        this.picPelanggan = picPelanggan;
    }

    public String getTeleponPelanggan() {
        return teleponPelanggan;
    }

    public void setTeleponPelanggan(String teleponPelanggan) {
        this.teleponPelanggan = teleponPelanggan;
    }

    public String getKeteranganKunjunganPelanggan() {
        return keteranganKunjunganPelanggan;
    }

    public void setKeteranganKunjunganPelanggan(String keteranganKunjunganPelanggan) {
        this.keteranganKunjunganPelanggan = keteranganKunjunganPelanggan;
    }

    public String getTipePelanggan() {
        return tipePelanggan;
    }

    public void setTipePelanggan(String tipePelanggan) {
        this.tipePelanggan = tipePelanggan;
    }
}
