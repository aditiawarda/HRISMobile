package com.gelora.absensi.model;


public class DataDetailSerahTerima {

    private String id;
    private String id_core;
    private String id_st;
    private String serah_terima_detail;
    private String saat_masuk;
    private String saat_keluar;
    private String keterangan;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getId_core() {
        return id_core;
    }

    public void setId_core(String id_core) {
        this.id_core = id_core;
    }

    public String getId_st() {
        return id_st;
    }

    public void setId_st(String id_st) {
        this.id_st = id_st;
    }

    public String getSerah_terima_detail() {
        return serah_terima_detail;
    }

    public void setSerah_terima_detail(String serah_terima_detail) {
        this.serah_terima_detail = serah_terima_detail;
    }

    public String getSaat_masuk() {
        return saat_masuk;
    }

    public void setSaat_masuk(String saat_masuk) {
        this.saat_masuk = saat_masuk;
    }

    public String getSaat_keluar() {
        return saat_keluar;
    }

    public void setSaat_keluar(String saat_keluar) {
        this.saat_keluar = saat_keluar;
    }

    public String getKeterangan() {
        return keterangan;
    }

    public void setKeterangan(String keterangan) {
        this.keterangan = keterangan;
    }

}
