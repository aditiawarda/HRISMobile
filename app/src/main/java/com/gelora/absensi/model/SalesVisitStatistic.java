package com.gelora.absensi.model;


public class SalesVisitStatistic {

    private String idSales;
    private String jumlah_kunjungan;
    private String namaKaryawan;
    private String avatar;
    private String wilayah;

    public String getIdSales() {
        return idSales;
    }

    public void setIdSales(String idSales) {
        this.idSales = idSales;
    }

    public String getJumlah_kunjungan() {
        return jumlah_kunjungan;
    }

    public void setJumlah_kunjungan(String jumlah_kunjungan) {
        this.jumlah_kunjungan = jumlah_kunjungan;
    }

    public String getNamaKaryawan() {
        return namaKaryawan;
    }

    public void setNamaKaryawan(String namaKaryawan) {
        this.namaKaryawan = namaKaryawan;
    }

    public String getAvatar() {
        return avatar;
    }

    public void setAvatar(String avatar) {
        this.avatar = avatar;
    }

    public String getWilayah() {
        return wilayah;
    }

    public void setWilayah(String wilayah) {
        this.wilayah = wilayah;
    }

}