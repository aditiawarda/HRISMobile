package com.gelora.absensi.model;


public class ProductSuma {

    private String id_produk;
    private String id_produk_nav;
    private String judul;
    private String nama_produk_nav;
    private String beban;
    private String unit;
    private String price;
    private String image;
    private String sub_id_produk;

    public String getId_produk() {
        return id_produk;
    }

    public void setId_produk(String id_produk) {
        this.id_produk = id_produk;
    }

    public String getId_produk_nav() {
        return id_produk_nav;
    }

    public void setId_produk_nav(String id_produk_nav) {
        this.id_produk_nav = id_produk_nav;
    }

    public String getJudul() {
        return judul;
    }

    public void setJudul(String judul) {
        this.judul = judul;
    }

    public String getNama_produk_nav() {
        return nama_produk_nav;
    }

    public void setNama_produk_nav(String nama_produk_nav) {
        this.nama_produk_nav = nama_produk_nav;
    }

    public String getBeban() {
        return beban;
    }

    public void setBeban(String beban) {
        this.beban = beban;
    }

    public String getUnit() {
        return unit;
    }

    public void setUnit(String unit) {
        this.unit = unit;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getSub_id_produk() {
        return sub_id_produk;
    }

    public void setSub_id_produk(String sub_id_produk) {
        this.sub_id_produk = sub_id_produk;
    }
}
